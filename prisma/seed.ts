async function main() {
  console.log("بدء إضافة المقالات...");

  for (const article of articles) {
    const slug = generateSlug(article.title);

    // Check if article already exists
    const existing = await prisma.newsPost.findUnique({
      where: { slug },
    });

    if (existing) {
      console.log(`المقال "${article.title}" موجود بالفعل، يتم تخطيه.`);
      continue;
    }

    await prisma.newsPost.create({
      data: {
        title: article.title,
        slug: slug,
        content: article.content,
        excerpt: article.excerpt || null,
        author: article.author || null,
        tags: article.tags || [],
        metaDescription: article.metaDescription || null,
        metaKeywords: article.metaKeywords || [],
        published: false,
        publishedAt: null,
      },
    });

    console.log(`تمت إضافة المقال: "${article.title}"`);
  }

  console.log(`تم إضافة ${articles.length} مقال بنجاح!`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });












